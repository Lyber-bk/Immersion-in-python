# 2.–¢—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç —Ç–æ–ª—å–∫–æ —Ç–æ–≥–¥–∞, –∫–æ–≥–¥–∞ —Å—É–º–º–∞ –ª—é–±—ã—Ö –¥–≤—É—Ö –µ–≥–æ —Å—Ç–æ—Ä–æ–Ω –±–æ–ª—å—à–µ —Ç—Ä–µ—Ç—å–µ–π.
# –î–∞–Ω–æ a, b, c - —Å—Ç–æ—Ä–æ–Ω—ã –ø—Ä–µ–¥–ø–æ–ª–∞–≥–∞–µ–º–æ–≥–æ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞.
# –¢—Ä–µ–±—É–µ—Ç—Å—è —Å—Ä–∞–≤–Ω–∏—Ç—å –¥–ª–∏–Ω—É –∫–∞–∂–¥–æ–≥–æ –æ—Ç—Ä–µ–∑–∫–∞-—Å—Ç–æ—Ä–æ–Ω—ã —Å —Å—É–º–º–æ–π –¥–≤—É—Ö –¥—Ä—É–≥–∏—Ö.
# –ï—Å–ª–∏ —Ö–æ—Ç—è –±—ã –≤ –æ–¥–Ω–æ–º —Å–ª—É—á–∞–µ –æ—Ç—Ä–µ–∑–æ–∫ –æ–∫–∞–∂–µ—Ç—Å—è –±–æ–ª—å—à–µ —Å—É–º–º—ã –¥–≤—É—Ö –¥—Ä—É–≥–∏—Ö, —Ç–æ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞ —Å —Ç–∞–∫–∏–º–∏ —Å—Ç–æ—Ä–æ–Ω–∞–º–∏ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç.
# –û—Ç–¥–µ–ª—å–Ω–æ —Å–æ–æ–±—â–∏—Ç—å —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫ —Ä–∞–∑–Ω–æ—Å—Ç–æ—Ä–æ–Ω–Ω–∏–º, —Ä–∞–≤–Ω–æ–±–µ–¥—Ä–µ–Ω–Ω—ã–º –∏–ª–∏ —Ä–∞–≤–Ω–æ—Å—Ç–æ—Ä–æ–Ω–Ω–∏–º.

a = float(input('–í–≤–µ–¥–∏—Ç–µ —Å—Ç–æ—Ä–æ–Ω—É —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞ a: '))
b = float(input('–í–≤–µ–¥–∏—Ç–µ —Å—Ç–æ—Ä–æ–Ω—É —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞ b: '))
c = float(input('–í–≤–µ–¥–∏—Ç–µ —Å—Ç–æ—Ä–æ–Ω—É —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞ c: '))

if a == 0 or b == 0 or c == 0:
    print('–í–≤–µ–¥–∏—Ç–µ –≤—Å–µ –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è')
elif a > (b + c):
    print('–¢–∞–∫–æ–≥–æ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞ –Ω–µ –±—ã–≤–∞–µ—Ç')
elif b > (a + c):
    print('–¢–∞–∫–æ–≥–æ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞ –Ω–µ –±—ã–≤–∞–µ—Ç')
elif c > (b + a):
    print('–¢–∞–∫–æ–≥–æ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞ –Ω–µ –±—ã–≤–∞–µ—Ç')
elif a == b == c:
    print('–≠—Ç–æ —Ä–∞–≤–Ω–æ—Å—Ç–æ—Ä–æ–Ω–Ω–∏–π —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫')
elif a == b or b == c or a == c:
    print('–¢—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫ —Ä–∞–≤–Ω–æ–±–µ–¥—Ä–µ–Ω–Ω—ã–π')
elif a != b and a != c and b != c:
    print('–¢—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫ —Ä–∞–∑–Ω–æ—Å—Ç–æ—Ä–æ–Ω–Ω–∏–π')

# 3. –ù–∞–ø–∏—à–∏—Ç–µ –∫–æ–¥, –∫–æ—Ç–æ—Ä—ã–π –∑–∞–ø—Ä–∞—à–∏–≤–∞–µ—Ç —á–∏—Å–ª–æ –∏ —Å–æ–æ–±—â–∞–µ—Ç —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –æ–Ω–æ –ø—Ä–æ—Å—Ç—ã–º –∏–ª–∏ —Å–æ—Å—Ç–∞–≤–Ω—ã–º.
# –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –ø—Ä–∞–≤–∏–ª–æ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏: ‚Äú–ß–∏—Å–ª–æ —è–≤–ª—è–µ—Ç—Å—è –ø—Ä–æ—Å—Ç—ã–º, –µ—Å–ª–∏ –¥–µ–ª–∏—Ç—Å—è –Ω–∞—Ü–µ–ª–æ —Ç–æ–ª—å–∫–æ –Ω–∞ –µ–¥–∏–Ω–∏—Ü—É –∏ –Ω–∞ —Å–µ–±—è‚Äù.
# –°–¥–µ–ª–∞–π—Ç–µ –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–µ –Ω–∞ –≤–≤–æ–¥ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—ã—Ö —á–∏—Å–µ–ª –∏ —á–∏—Å–µ–ª –±–æ–ª—å—à–µ 100 —Ç—ã—Å—è—á.

MIN_LIMIT = 0
MAX_LIMIT = 100000
MIN_PRIME_NUM = 2

number = -1
while not (MIN_LIMIT < number and number <= MAX_LIMIT):                 # –ü—Ä–æ–≤–µ—Ä—è–µ–º –≤–≤–µ–¥–µ–Ω–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ —á—Ç–æ–±—ã –ø–æ–ø–∞–ª–∏ –≤ –¥–∏–∞–ø–∞–∑–æ–Ω
    number = int(input('–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ –≤ –¥–∏–∞–ø–∞–∑–æ–Ω–µ –æ—Ç 1 –¥–æ 100 —Ç—ã—Å.: '))
if number < MIN_PRIME_NUM:                                              # –ü—Ä–æ–≤–µ—Ä–∫–∞ —á–∏—Å–ª–∞ –µ—Å–ª–∏ –º–µ–Ω—å—à–µ 2
    print("–ß–∏—Å–ª–æ –Ω–µ —è–≤–ª—è–µ—Ç—Å—è –Ω–∏ –ø—Ä–æ—Å—Ç—ã–º, –Ω–∏ —Å–æ—Å—Ç–∞–≤–Ω—ã–º")
else:
    is_prime = True
    for i in range(MIN_PRIME_NUM, int(number / 2) + 1):     # –ü—Ä–æ—Ö–æ–¥–∏–º —Ü–∏–∫–ª–æ–º –¥–æ –ø–æ–ª–æ–≤–∏–Ω—ã –∑–∞–¥–∞–Ω–Ω–æ–≥–æ —á–∏—Å–ª–∞
        if number % i == 0:                                 # –ü—Ä–æ–≤–µ—Ä—è–µ–º –¥–µ–ª–µ–Ω–∏–µ –±–µ–∑ –æ—Å—Ç–∞—Ç–∫–∞
            is_prime = False                                # –ü—Ä–µ—Ä—ã–≤–∞–µ–º —Ü–∏–∫–ª –µ—Å–ª–∏ –¥–µ–ª–∏—Ç—Å—è –±–µ–∑ –æ—Å—Ç–∞—Ç–∫–∞
            break

    if is_prime:
        print('–ß–∏—Å–ª–æ –ø—Ä–æ—Å—Ç–æ–µ')
    else:
        print('–ß–∏—Å–ª–æ —Å–æ—Å—Ç–∞–≤–Ω–æ–µ')

# –ó–∞–¥–∞–Ω–∏–µ No3
# üìå –ü—Ä–æ–≥—Ä–∞–º–º–∞ –∑–∞–≥–∞–¥—ã–≤–∞–µ—Ç —á–∏—Å–ª–æ –æ—Ç 0 –¥–æ 1000. –ù–µ–æ–±—Ö–æ–¥–∏–º–æ —É–≥–∞–¥–∞—Ç—å —á–∏—Å–ª–æ –∑–∞ 10 –ø–æ–ø—ã—Ç–æ–∫.
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –ø–æ–¥—Å–∫–∞–∑—ã–≤–∞—Ç—å ¬´–±–æ–ª—å—à–µ¬ª –∏–ª–∏ ¬´–º–µ–Ω—å—à–µ¬ª –ø–æ—Å–ª–µ –∫–∞–∂–¥–æ–π –ø–æ–ø—ã—Ç–∫–∏.
# üìå –î–ª—è –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ —Å–ª—É—á–∞–π–Ω–æ–≥–æ —á–∏—Å–ª–∞ –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–æ–¥:


from random import randint

LOWER_LIMIT = 0
UPPER_LIMIT = 1000

num = randint(LOWER_LIMIT, UPPER_LIMIT)
attempts = 10

attempt = 1
while attempt <= attempts:
    number = int(input(f'–ü–æ–ø—ã—Ç–∫–∞ {attempt} –í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ: '))
    if number < num:
        print('–ë–æ–ª—å—à–µ')
    elif number > num:
        print('–ú–µ–Ω—å—à–µ')
    else:
        print('–£–≥–∞–¥–∞–ª')
        break
    attempt += 1
else:
    print('–ö–æ–Ω—á–∏–ª–∏—Å—å –ø–æ–ø—ã—Ç–∫–∏')